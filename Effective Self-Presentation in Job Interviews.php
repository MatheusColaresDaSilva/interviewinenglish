                        <h6 style="line-height: 25px;" data-aos="fade-up" data-aos-delay="500" data-aos-duration="500" data-aos-easing="ease-in-out">Are you nervous about a job interview? Don’t worry! With a little practice and preparation, you can feel ready and confident. As a native English tutor for interview practice, I help people like you improve their English and feel confident in job interviews. Here are some easy tips to help you:</h6>
                        <img class="img-fluid rounded w-100 mb-4" data-aos="fade-up" data-aos-delay="500" data-aos-duration="500" data-aos-easing="ease-in-out" src="img/blog-3.jpg" alt="Image">
                        <h2 class="text-primary mt-3" >1. Learn About the Job</h2>
                        <h6 style="line-height: 25px;">
                            Find out what the company does and what the job needs. This will help you give better answers.
                            Tip: Look at the company’s website or ask your friends if they know anything about it.

                        </h6>
                        <h2 class="text-primary mt-3">2. Dress Nicely</h2>
                        <h6 style="line-height: 25px;">
                            Wear clean, neat clothes that match the job. A suit is good for formal jobs, but business casual might be okay for      creative jobs.
                            Tip: If you’re not sure, it’s better to dress a little more formal.

                        </h6>

                        <h2 class="text-primary mt-3" data-aos="fade-up" data-aos-delay="500" data-aos-duration="500" data-aos-easing="ease-in-out">3. Speak Clearly</h2>
                        <h6 style="line-height: 25px;">
                            When answering questions, talk slowly and clearly. This helps the interviewer understand you better.
                            Tip: Practice with an English speaking coach for job interviews to improve your confidence.

                        </h6>

                        <h2 class="text-primary mt-3">4. Use Good Body Language</h2>
                        <h6 style="line-height: 25px;">
                        Sit up straight, smile, and make eye contact. This shows that you are confident and polite.
Tip: Practice in front of a mirror to see how you look.

                        </h6>

                        <h2 class="text-primary mt-3" data-aos="fade-up" data-aos-delay="500" data-aos-duration="500" data-aos-easing="ease-in-out">5. Prepare a Short Introduction</h2>
                        <h6 style="line-height: 25px;">
                        Be ready to answer “Tell me about yourself.” Talk about your work, your skills, and why you want the job.
Tip: A business English interview coach can help you practice this.

                        </h6>

                        <h2 class="text-primary mt-3">6. Show Your Skills</h2>
                        <h6 style="line-height: 25px;">
                        Talk about what you are good at. For example, “I am very organized” or “I helped my team finish a big project.”
Tip: Use examples and numbers, like “I saved the company $5,000 by finding better suppliers.”


                        </h6>

                        <h2 class="text-primary mt-3">7. Ask Questions</h2>
                        <h6 style="line-height: 25px;">
                        Show interest in the job by asking questions like, “What does a normal day look like in this job?”
Tip: Prepare 2-3 questions before the interview.


                        </h6>


                        <h2 class="text-primary mt-3">8. End the Interview Well</h2>
                        <h6 style="line-height: 25px;">
                        Say thank you and tell them you are excited about the job. You can also ask when you will hear back from them.
Tip: Send a thank-you email after the interview.


                        </h6>

