提示：面试前准备2-3个问题。


                        </h6>


                        <h2 class="text-primary mt-3">8. 好好结束面试</h2>
                        <h6 style="line-height: 25px;">
                        说谢谢并告诉他们你对这份工作感到兴奋。您还可以询问何时会收到他们的回复。
